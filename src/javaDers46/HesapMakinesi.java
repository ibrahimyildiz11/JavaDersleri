package javaDers46;

public class HesapMakinesi {
    int toplama(int s1, int s2) {
        int toplam = s1 + s2;
        return toplam;
    }

    int cikarma(int s1, int s2) {
        int fark = s1 - s2;
        return fark;
    }

    int carpma(int s1, int s2) {
        int carpim = s1 * s2;
        return carpim;
    }

    int bolme(int s1, int s2) {
        int bolum = s1 / s2;
        return bolum;
    }
}
