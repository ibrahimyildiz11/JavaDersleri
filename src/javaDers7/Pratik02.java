package javaDers7;

public class Pratik02 {
    public static void main(String[] args) {
        /*Kisa kenarin uzunlugu10,uzun kenarin uzunlugu 25 olan dikdortgenin
        alanini ve cevresini hesaplayan bir program yaziniz.*/

        int kisakenar = 10;
        int uzunkenar = 25;

        int alan = kisakenar * uzunkenar;
        int cevre = 2* kisakenar + 2* uzunkenar;

        System.out.println("Alan : " + alan);
        System.out.println("Cevre : " + cevre);
    }
}
