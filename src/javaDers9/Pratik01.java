package javaDers9;

public class Pratik01 {
    public static void main(String[] args) {
        //ARTIMETIK OPRATORS

        int s1 = 25;
        int s2 = 5;

        s1 += s2;
        System.out.println(s1);

        s1 -= s2;
        System.out.println(s1);

        s1 *= s2;
        System.out.println(s1);

        s1 /= s2;
        System.out.println(s1);


    }
}
